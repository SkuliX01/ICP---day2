<template>
    <div>
        <button @click="pobierzWpisy">Odczytaj Wpisy</button>
        <h1>siema blog</h1>
        {{ wpisy }}
        <input type="text" v-model="nowyBlog">
        <button @click="dodajWpis">Dodaj nowy wpis</button>
    </div>
</template>

<script>

    import { dzien2_backend } from 'declarations/dzien2_backend/index';

    export default {
        data() {
            return {
                wpisy: [],
                nowyBlog: ""
            }
        },
        methods: {
            async pobierzWpisy(){
                this.wpisy = await dzien2_backend.odczytaj_wpisy()
            },
            async dodajWpis(){
                await dzien2_backend.dodaj_wpis(this.nowyBlog)
            }
        }
    }
</script>